                      recursos físicos que executam tarefas. Linux é um sistema operacional que tem ganhado cada vez mais espaço nos datacenters de empresas de todos os 
                      tamanhos. São muitas as vantagens de usar Linux: além de ser uma solução de baixo custo, a plataforma se diferencia por ser uma solução altamente 
